package chapter02;

public class ConsoleOutput01 {
	public static void main(String[] args) {
		System.out.println("안녕 ");		//println : 출력 후 한줄띄기
		System.out.println("하세요");
		
		System.out.print("안녕 ");		//print : 출력
		System.out.print("하세요");
		
		System.out.println();
		
		System.out.print("안녕 \n");		// \n : 한줄띄기
		System.out.println("하세요.");
	}
}
